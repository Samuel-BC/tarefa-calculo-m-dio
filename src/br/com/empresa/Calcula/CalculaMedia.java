package br.com.empresa.Calcula;

public class CalculaMedia {

    public static void main(String[] args) {

        int nota1 = 10;
        int nota2  = 9;
        int nota3 = 8;
        int nota4 = 7;

        double total = nota1 + nota2 + nota3 + nota4;

        int media = 4;

        double media1 = total / media;

        System.out.println("Media: " + media1);

    }
}
